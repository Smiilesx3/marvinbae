package calculator;

public class Calculator {
    public double methodeAddition(double Zahl1, double Zahl2) {
        return Zahl1 + Zahl2;
    }

    public double methodeSubtraction(double Zahl1, double Zahl2) {
        return Zahl1 - Zahl2;
    }

    public double methodeMulitplication(double Zahl1, double Zahl2) {
        return Zahl1 * Zahl2;
    }

    public double methodeDivision(double Zahl1, double Zahl2) {
        return Zahl1 / Zahl2;
    }

}
